    public void dibujarTabla(){
        int count = 0;
        for (int fila = 0; fila < filas; fila++) {
            for (int columna = 0; columna < columnas; columna++) {
                botones[fila][columna] = new Componente(45 * columna, 32 * fila, 55, 45);
                //botones[fila][columna].cambiarNombre(fila,columna);
                jPanel1.add(botones[fila][columna]);
                if (fila == 0 && columna == 0) {
                    botones[fila][columna].setHorizontalAlignment(SwingConstants.CENTER);
                    botones[fila][columna].setX("X");
                } else {
                    //Border border = BorderFactory.createLineBorder(Color.WHITE, 5);

                    //botones[fila][columna].setOpaque(true);
                    if (fila == 0) {
                        //botones[fila][columna].setBackground(Color.red);
                        botones[fila][columna].setFont(new java.awt.Font("Tahoma", 1, 14)); // NOI18N
                        botones[fila][columna].setHorizontalAlignment(SwingConstants.CENTER);
                        //botones[fila][columna].setBorder(border);
                        botones[fila][columna].setForeground(Color.white);
                        botones[fila][columna].setFilaInicio(count);
                    }
                    if (columna == 0) {
                        //botones[fila][columna].setBackground(Color.red);
                        botones[fila][columna].setFont(new java.awt.Font("Tahoma", 1, 14)); // NOI18N
                        botones[fila][columna].setHorizontalAlignment(SwingConstants.CENTER);
                        //botones[fila][columna].setBorder(border);
                        botones[fila][columna].setForeground(Color.white);
                        botones[fila][columna].setColumnaInicio(fila - 1);
                    }
                    count++;
                }
            }
        }
    }
    
