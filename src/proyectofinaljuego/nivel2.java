    public void diccionario_respuestas(int multiplicando, int multiplicador, int respuesta){
            for(int  fila=0; fila<filas; fila++){
            for(int columna=0; columna<columnas; columna++){
                   // botones[0][1].setForeground(Color.yellow); 
                    botones[multiplicando][multiplicador].setFont(new java.awt.Font("Tahoma", 1, 14)); // NOI18N
                    botones[multiplicando][multiplicador].setHorizontalAlignment(SwingConstants.CENTER);
                        //botones[fila][columna].setBorder(border);
                    botones[multiplicando][multiplicador].setForeground(Color.blue);                    
                    botones[multiplicando][multiplicador].setX(String.valueOf(respuesta));        
            }}                  
                        
    }
    
    
